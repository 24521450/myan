def main() -> int:
    ap = argparse.ArgumentParser(description='Apply P1 semantic gloss cleanup.')
    ap.add_argument('--dry-run', action='store_true',
                    help='Preview without writing.')
    ap.add_argument('--targets', nargs='*',
                    choices=['master', 'filled', 'txt'],
                    help='Limit which files to touch (default: all).')
    args = ap.parse_args()

    targets = args.targets or ['master', 'filled', 'txt']

    print('=' * 72)
    print(f'P1 Semantic Gloss Cleanup  (dry-run={args.dry_run})')
    print(f'Timestamp: {_ts()}')
    print('=' * 72)

    # Step 0: compute the fix map from the master audit ONCE.
    # This ensures master/filled/TXT all see the same set of fixes.
    print(f'\n[0/3] Computing fix map from {MASTER_AUDIT.name}...')
    fix_map = compute_fix_map(MASTER_AUDIT)
    print(f'  unique (word, pos, cefr) keys with fixes: {len(fix_map)}')

    # --- Audit: master ---
    if 'master' in targets:
        print(f'\n[1/3] {MASTER_AUDIT.name}')
        if not args.dry_run:
            bak = backup(MASTER_AUDIT)
            print(f'  backup: {bak.name}')
        s = apply_audit(MASTER_AUDIT, dry_run=args.dry_run, fix_map=fix_map)
        print(f"  rows: {s['total']}  bad: {s['bad_rows']}  fixed: {s['fixed']}  "
              f"no_fix: {s['no_fix_available']}  old_mismatch: {s['old_value_mismatch']}")

    # --- Audit: filled ---
    if 'filled' in targets:
        print(f'\n[2/3] {EXPANDED_FILLED.name}')
        if not args.dry_run:
            bak = backup(EXPANDED_FILLED)
            print(f'  backup: {bak.name}')
        s = apply_audit(EXPANDED_FILLED, dry_run=args.dry_run, fix_map=fix_map)
        print(f"  rows: {s['total']}  bad: {s['bad_rows']}  fixed: {s['fixed']}  "
              f"no_fix: {s['no_fix_available']}  old_mismatch: {s['old_value_mismatch']}")

    # --- TXT ---
    if 'txt' in targets:
        print(f'\n[3/3] {DECK_TXT.name}')
        if not args.dry_run:
            bak = backup(DECK_TXT)
            print(f'  backup: {bak.name}')
        s = apply_txt(DECK_TXT, dry_run=args.dry_run, fix_map=fix_map)
        print(f"  rows: {s['total']}  with_def: {s['rows_with_def']}  "
              f"updated: {s['updated']}  no_match: {s['no_match']}")

    print('\nDone.')
    return 0


if __name__ == '__main__':
    raise SystemExit(main())
