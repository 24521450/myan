# Audit JSONL files
# =============================================================================

def chunk_count(g: str) -> int:
    import re
    return len([c for c in re.split(r'\s*[|;]\s*', g.strip()) if c.strip()])


def is_bad(r: dict) -> bool:
    """Check if a row's gloss_after fails validator (post-P0 hygiene)."""
    g = r.get('gloss_after') or ''
    if not (isinstance(g, str) and g.strip()):
        return False
    res = normalize_gloss(g)
    v = validate_verdict(r['word'], res.gloss, res.separator, chunk_count(res.gloss))
    return bool(v)


def apply_audit(path: Path, *, dry_run: bool, fix_map: dict[tuple, str]) -> dict:
    """Apply P1 fixes to an audit JSONL file.

    Uses a pre-computed fix_map (key → new_norm). For each row:
      - if currently passes validator: skip (don't touch clean rows)
      - if currently fails validator AND key in fix_map: update
      - if currently fails validator AND key not in fix_map: leave as-is
        (engine couldn't generate a fix — counts as 'no_fix_available')

    Returns stats dict.
    """
    stats = Counter(
        total=0,
        bad_rows=0,
        fixed=0,
        no_fix_available=0,
        old_value_mismatch=0,
        clean_skipped=0,
    )

    rows_in = []
    with path.open(encoding='utf-8') as fp:
        for line in fp:
            if not line.strip():
                continue
            rows_in.append(json.loads(line))

    new_rows = []
    for r in rows_in:
        stats['total'] += 1
        key_override = (r['word'], r['pos'], r['cefr'])
        if not is_bad(r) and key_override not in EXPLICIT_FIXES:
            stats['clean_skipped'] += 1
            new_rows.append(r)
            continue
        stats['bad_rows'] += 1

        old_gloss = r['gloss_after']
        old_norm = normalize_gloss(old_gloss).gloss
        key = (r['word'], r['pos'], r['cefr'], old_norm)
        if key not in fix_map:
            stats['no_fix_available'] += 1
            new_rows.append(r)
            continue

        new_gloss = fix_map[key]

        if old_norm == new_gloss:
            stats['old_value_mismatch'] += 1
            new_rows.append(r)
            continue

        r['gloss_after'] = new_gloss
        # Recompute separator + word count via normalize.
        res = normalize_gloss(new_gloss)
        r['separator'] = res.separator
        r['gloss_word_count'] = res.gloss_word_count
        # Mark fix_status so downstream knows it came from P1 path.
        r['fix_status'] = 'p1_rewritten'
        stats['fixed'] += 1
        new_rows.append(r)

    if not dry_run and stats['fixed']:
        tmp = path.with_suffix(path.suffix + '.tmp_p1')
        with tmp.open('w', encoding='utf-8', newline='') as fp:
            for r in new_rows:
                fp.write(json.dumps(r, ensure_ascii=False))
                fp.write('\n')
        tmp.replace(path)

    return dict(stats)


