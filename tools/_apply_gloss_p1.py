"""Apply P1 semantic gloss cleanup.

Per user plan (2026-06-21, "P1 Semantic Gloss Cleanup"):

  Rewrite bad glosses (that pass P0 hygiene but fail validator) so every
  touched row passes ``validate_verdict()``:

  - total words 1-6
  - '|' chunks 1-4 words each (max 2 sides)
  - ';' chunks 1-3 words each (max 2 sides)
  - no headword in chunk
  - no morphological near-self
  - no visible POS labels

  The rewrite is generated by ``src.deck_builder.gloss_p1.fix_one`` —
  a heuristic engine with explicit FIXES overrides for hard cases.

Touches (with explicit old-value guard — key = (word, pos, cefr, old_gloss)):
  - data/audit_full_deck_v2.jsonl
  - data/audit_expanded_needs_gloss_filled.jsonl
  - English Academic Vocabulary.txt  (Definition column)

Backups:
  - Each target gets a .bak_pre_gloss_p1_<timestamp> next to it.

Safety:
  - Default refuses to write unless --apply is given. Use --dry-run to preview.
  - Old-value guard: a row's gloss is only updated if the current gloss_after
    matches the engine's expected input. This prevents accidentally rewriting
    a row that was already fixed by another path.

Out of scope (P2):
  - 5 duplicate (word, pos, cefr) keys
"""
