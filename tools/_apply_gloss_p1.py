def apply_txt(path: Path, *, dry_run: bool, fix_map: dict[tuple, str]) -> dict:
    """Apply P1 fixes to the Definition column (index 6) of the TXT deck file.

    Uses a pre-computed fix_map (key → new_norm). Joins via
    (word, pos, cefr, old_gloss_after) key.

    Only updates a def cell if:
      - key is in fix_map
    """
    stats = Counter(
        total=0,
        rows_with_def=0,
        updated=0,
        no_match=0,
        mismatch=0,
    )

    raw_lines = path.read_text(encoding='utf-8').splitlines(keepends=False)
    header_lines = []
    body_start = 0
    for i, line in enumerate(raw_lines):
        if line.startswith('#'):
            header_lines.append(line)
        else:
            body_start = i
            break
    else:
        body_start = len(raw_lines)

    body_lines = raw_lines[body_start:]
    new_body = []
    for line in body_lines:
        stats['total'] += 1
        if not line.strip():
            new_body.append(line)
            continue
        fields = line.split('\t')
        if len(fields) <= 6:
            new_body.append(line)
            continue
        stats['rows_with_def'] += 1

        word = fields[3]
        pos = fields[4]
        cefr = fields[14] if len(fields) > 14 else ''
        current_def = normalize_gloss(fields[6]).gloss
        key = (word, pos, cefr, current_def)

        new_gloss = fix_map.get(key)
        if new_gloss is None:
            stats['no_match'] += 1
            new_body.append(line)
            continue

        fields[6] = new_gloss
        new_line = '\t'.join(fields)
        new_body.append(new_line)
        stats['updated'] += 1

    if not dry_run and stats['updated']:
        all_lines = header_lines + new_body
        text_out = '\n'.join(all_lines) + '\n'
        tmp = path.with_suffix(path.suffix + '.tmp_p1')
        tmp.write_text(text_out, encoding='utf-8')
        tmp.replace(path)

    return dict(stats)


def compute_fix_map(master_path: Path) -> dict[tuple, str]:
    """Compute the (word, pos, cefr, old_gloss_after) → new_norm map from the master audit.

    Only considers rows whose CURRENT gloss fails the validator — clean rows
    are NOT in the map (they're already valid; the engine shouldn't pretend
    they need fixing).

    If multiple rows share the same key, keep the LAST one in file order
    (master is sorted deterministically by (word, source_files[0]) per the
    rebuild contract).
    """
    fix_map: dict[tuple, str] = {}
    with master_path.open(encoding='utf-8') as fp:
        for line in fp:
            if not line.strip():
                continue
            r = json.loads(line)
            old = r.get('gloss_after') or ''
            if not old.strip():
                continue
            # Skip rows that already pass validator — we don't want to "fix"
            # clean glosses unless explicitly requested in EXPLICIT_FIXES.
            key_override = (r['word'], r['pos'], r['cefr'])
            if not is_bad(r) and key_override not in EXPLICIT_FIXES:
                continue
            new = fix_one(
                r['word'], r['pos'], r['cefr'],
                old, r.get('def_before', ''),
            )
            if new is None:
                continue
            new_norm = normalize_gloss(new).gloss
            old_norm = normalize_gloss(old).gloss
            if new_norm == old_norm:
                continue
            key = (r['word'], r['pos'], r['cefr'], old_norm)
            fix_map[key] = new_norm
    return fix_map


